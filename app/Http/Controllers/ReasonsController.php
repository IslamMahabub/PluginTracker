    public function reasonslist(Request $request)
    {
        $postData       = $request->all();
        $startDate      = ($postData['start_date']) ? date('Y-m-d', strtotime($postData['start_date'])) : '';
        $endDate        = $postData['end_date'] ? date('Y-m-d', strtotime($postData['end_date'])) : '';
        $plugin         = $postData['plugins'];
        $reason         = $postData['reason'];

        if($startDate > $endDate){
            $this->index();
        }
        if($plugin) {
            $pluginInfo                 = $this->pluginsInfo($plugin, $startDate, $endDate, $reason);
            $reasonWiseUninstallData    = $pluginInfo['pluginReasonWiseUninstallData'];
        }else{
            $pluginInfo                 = $this->pluginsInfo('', $startDate, $endDate, $reason);
            $reasonWiseUninstallData    = $pluginInfo['reasonWiseUninstallData'];
        }

        $reasonList = array();
        foreach ($reasonWiseUninstallData as $result){
            $server_info = unserialize($result->server_info);
            $reasonList['data'][] = [
                'created_at'    => date('d-m-Y', strtotime($result->created_at)),
                'plugin'        => $result->plugin,
                'reason_id'     => ucfirst(str_replace('-', ' ', $result->reason_id)),
                'reason_info'   => $result->reason_info,
                'url'           => $result->url,
                'user_email'    => $result->user_email,
                'user_name'     => $result->user_name,
                'locale'        => $this->language($result->locale),
                'multisite'     => $result->multisite,
                'software'      => (array_key_exists('software', $server_info)) ? $server_info['software'] : '',
                'php_version'   => (array_key_exists('php_version', $server_info)) ? $server_info['php_version'] : '',
                'mysql_version' => (array_key_exists('mysql_version', $server_info)) ? $server_info['mysql_version'] : '',
                'wp_version'    => (array_key_exists('wp_version', $server_info)) ? $server_info['wp_version'] : '',
            ];
        }
        return json_encode($reasonList);
    }

