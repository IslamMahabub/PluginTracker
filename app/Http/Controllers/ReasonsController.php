    public function pluginsInfo($plugin, $startDate = '', $endDate = '', $reason = ''){

        //SQL condition
        $cond = '';
        if($startDate != '' && $endDate != ''){
            $cond                   = "WHERE DATE(created_at) BETWEEN '$startDate' AND '$endDate'";
            $condPlugin             = "WHERE DATE(created_at) BETWEEN '$startDate' AND '$endDate' AND plugin = '".$plugin."'";
        }
        else{
            $condPlugin             = "WHERE plugin = '".$plugin."'";
        }

        if($reason){
            $condPlugin = $condPlugin." AND reason_id = '$reason'";
            $cond       = ($cond) ? $cond." AND reason_id = '$reason'" : "WHERE reason_id = '$reason'";
        }

        //Plugin Uninstall Reason
        $reasonWiseUninstallData            = DB::select(DB::raw("SELECT * FROM uninstall_tracking ".$cond." ORDER BY id DESC" ));
        $pluginReasonWiseUninstallData      = DB::select(DB::raw("SELECT * FROM uninstall_tracking 
                                                        ".$condPlugin." ORDER BY id DESC" ));

        return array(
            'reasonWiseUninstallData'               => $reasonWiseUninstallData,
            'pluginReasonWiseUninstallData'         => $pluginReasonWiseUninstallData,
        );
    }

